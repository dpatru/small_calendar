#!/usr/bin/env python3
"""
Test different Unicode characters for calendar display
"""

print("=== CALENDAR CHARACTER OPTIONS ===\n")

print("1. BLOCK CHARACTERS (current):")
print("█ ▓ ▒ ░ ▄ ▀ ■ □ ▢ ▣ ▤ ▥ ▦ ▧ ▨ ▩")
print("Current: █ = First day, ▓ = Today, ░ = Regular\n")

print("2. GEOMETRIC SHAPES:")
print("● ○ ◐ ◑ ◒ ◓ ◔ ◕ ◖ ◗ ◘ ◙ ◚ ◛ ◜ ◝ ◞ ◟")
print("Example: ● = First day, ○ = Today, ◐ = Regular\n")

print("3. MATHEMATICAL SYMBOLS:")
print("• ◦ ◯ ◉ ◎ ◈ ◇ ◆ ◊ ◈ ◉ ◊ ○ ◌ ◍ ◎ ●")
print("Example: ● = First day, ◎ = Today, • = Regular\n")

print("4. DICE SYMBOLS:")
print("⚀ ⚁ ⚂ ⚃ ⚄ ⚅ ⚆ ⚇ ⚈ ⚉ ⚊ ⚋")
print("Example: ⚀ = First day, ⚁ = Today, ⚂ = Regular\n")

print("5. SIMPLE DOTS:")
print("· · · · · · · · · · · · · · · · · ·")
print("Example: · = All days (minimal)\n")

print("6. ALTERNATIVE BLOCKS:")
print("▣ ▤ ▥ ▦ ▧ ▨ ▩ ▪ ▫ ▬ ▭ ▮ ▯ ▰ ▱")
print("Example: ▣ = First day, ▤ = Today, ▥ = Regular\n")

print("7. CIRCLES AND SQUARES:")
print("◉ ◎ ◈ ◇ ◆ ◊ ◈ ◉ ◊ ○ ◌ ◍ ◎ ●")
print("Example: ◉ = First day, ◎ = Today, ○ = Regular\n")

print("8. MINIMAL STYLE:")
print("· · · · · · · · · · · · · · · · · ·")
print("Example: · = Regular, · = First day, · = Today (same character)\n")

print("=== RECOMMENDATIONS ===")
print("• Current (█▓░): Good contrast, clear hierarchy")
print("• Geometric (●○◐): More visual variety")
print("• Minimal (····): Clean, simple")
print("• Dice (⚀⚁⚂): Fun, but might be too busy") 